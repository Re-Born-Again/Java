package com.class_package.Streams;

public class DummyMovie {
    private String name;
    private int views;

    public DummyMovie(String name, int views) {
        this.name = name;
        this.views = views;
    }

    public String getName() {
        return name;
    }

    public int getViews() {
        return views;
    }
}
